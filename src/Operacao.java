public class Operacao {


    public static int Somar(int a, int b){
        return a+b;
    }
    public static int Subtrair(int a, int b){
        return a-b;
    }
    public static double Multiplicar(double a, double b){
        return a*b;
    }
    public static double Dividir(double a, double b){
        return a/b;
    }


}
